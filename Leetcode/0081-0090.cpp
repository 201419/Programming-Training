// https://leetcode-cn.com/problems/gray-code/
// 89. 格雷编码

class Solution {
public:
    vector<int> grayCode(int n) {
        vector<int> result;
        for (int i = 0; i < (1<<n); i++) {
            result.push_back(i^(i>>1));
        }
        return result;
    }
};

